	{
		String code = "";
		if(isContaint == true)
		{
			String initValue = value;
			for (String[] array : checkList)
			{
				if (initValue.equals(array[0]))
				{
					code = StringUtil.getNotNullStr(array[2]);
					break;
				}
			}
		}
		return code;  
	}
	
